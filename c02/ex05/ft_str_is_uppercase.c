int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	upper;

	upper = 1;
	i = 0 ;
	while (upper == 1 && str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			upper = 0;
		}
		i++;
	}
	return (upper);
}
