int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	lower;

	lower = 1;
	i = 0 ;
	while (lower == 1 && str[i] != '\0')
	{
		if (str[i] < 'a' || str[i] > 'z' )
		{
			lower = 0;
		}
		i++;
	}
	return (lower);
}
