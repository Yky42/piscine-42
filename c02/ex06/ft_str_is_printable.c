int	ft_str_is_printable(char *str)
{
	int	i;
	int	print;

	print = 1;
	i = 0 ;
	while (print == 1 && str[i] != '\0')
	{
		if (str[i] < ' ' || str[i] > '~')
		{
			print = 0;
		}
		i++;
	}
	return (print);
}
