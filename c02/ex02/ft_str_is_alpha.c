int	ft_str_is_alpha(char *str)
{
	int	i;
	int	alpha;

	alpha = 1;
	i = 0 ;
	while (alpha == 1 && str[i] != '\0')
	{
		if ((str[i] < 'A' || str[i] > 'Z') && (str[i] < 'a' || str[i] > 'z' ))
		{
			alpha = 0;
		}
		i++;
	}
	return (alpha);
}
