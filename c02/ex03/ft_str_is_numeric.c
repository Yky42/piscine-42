int	ft_str_is_numeric(char *str)
{
	int	i;
	int	num;

	num = 1;
	i = 0;
	while (num == 1 && str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
		{
			num = 0;
		}
		i++;
	}
	return (num);
}
